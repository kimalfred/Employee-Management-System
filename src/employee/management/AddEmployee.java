    private void add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add1ActionPerformed
        try {
            String employeeId = emp.getText();
            String name = nameadd.getText();
            String email = emailadd.getText();
            String number = cnum.getText();
            String position = (String) post.getSelectedItem();
            String department = (String) dept.getSelectedItem();
            String password = pass.getText();
            String usertype = (String) user.getSelectedItem();

            if (employeeId.isEmpty() || name.isEmpty() || email.isEmpty() || number.isEmpty() ||
                    position.isEmpty() || department.isEmpty() || password.isEmpty() || usertype.isEmpty()) {

                JOptionPane.showMessageDialog(this, "Please fill in all fields", "Incomplete Information", JOptionPane.WARNING_MESSAGE);
            } else {
                String url = "jdbc:mysql://localhost/javadb";
                String dbUsername = "root";
                String dbPassword = "";

                try (Connection con = DriverManager.getConnection(url, dbUsername, dbPassword)) {
                    String insertQuery = "INSERT INTO emptb (employee_id, name, email, number, position, department, password, usertype) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

                    try (PreparedStatement pst = con.prepareStatement(insertQuery)) {
                        pst.setString(1, employeeId);
                        pst.setString(2, name);
                        pst.setString(3, email);
                        pst.setString(4, number);
                        pst.setString(5, position);
                        pst.setString(6, department);
                        pst.setString(7, password);
                        pst.setString(8, usertype);

                        int rowsAffected = pst.executeUpdate();

                        if (rowsAffected > 0) {
                            JOptionPane.showMessageDialog(this, "Data added successfully!");
                        } else {
                            JOptionPane.showMessageDialog(this, "Failed to add data.");
                        }
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "An error occurred. Please check console for details.");
                }
            }
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
            } catch (SQLException e) {
            }
        }

    }//GEN-LAST:event_add1ActionPerformed

