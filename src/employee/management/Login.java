    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try {
            String query = "SELECT * FROM `emptb` WHERE email=? and password =? and usertype=?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/javadb", "root", "");
            pst = con.prepareStatement(query);
            pst.setString(1, email.getText());
            pst.setString(2, password.getText());
            pst.setString(3, String.valueOf(select.getSelectedItem()));
            rs = pst.executeQuery();

        if (rs.next()) {
            String userType = rs.getString("usertype");
            JOptionPane.showMessageDialog(this, "Welcome! You are Logged in as " + userType);

            if ("Admin".equals(userType)) {
                AdminPanel a = new AdminPanel();
                a.setVisible(true);
                this.setVisible(false);
            } else if ("Manager".equals(userType)) {
                ManagerPanel u = new ManagerPanel();
                u.email.setText(email.getText());
                u.setVisible(true);
                this.setVisible(false);
            } else if ("Employee".equals(userType)) {
                UserPanel e = new UserPanel();
                e.setVisible(true);
                this.setVisible(false);
            }

            } else {
                JOptionPane.showMessageDialog(this, "UserName or Password do not Matched!!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_loginActionPerformed

